# Faça um Programa que peça dois números,realize as principais operações soma, subtração, multiplicação, divisão.

numero1 = float(input('Digite o primeiro número: '))
numero2 = float(input('Digite o segundo número: '))


# soma
soma = numero1 + numero2
print(f'A soma de {numero1} com {numero2} é {soma}')

# subtração
subtracao = numero1 - numero2
print(f'A subtração de {numero1} com {numero2} é {subtracao}')

#multiplicação
multiplicacao = numero1 * numero2
print(f'A multiplicação de {numero1} por {numero2} é {multiplicacao}')

# divisão
divisao = numero1 / numero2
print(f'A divisão de {numero1} por {numero2} é {divisao}')
